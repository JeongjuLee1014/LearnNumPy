import numpy as np

print("Generate range of 0 ~ 30, interval of elements is 2")
print(np.arange(0, 30, 2))
print()

print("Create an evenly spaced array within a range 0 ~ 1 , 5")
print(np.linspace(0, 1, 5))
print()

print("Generate an array of logarithm scale, evenly spaced within a range")
print(np.logspace(0.1, 1, 20))
print()
